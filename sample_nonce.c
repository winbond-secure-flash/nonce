
#include <stdint.h>

uint64_t PLAT_GetNONCE(void)
{
    /*-----------------------------------------------------------------------------------------------------*/
    /* We use here 64bit XORSHIFT algorithm for PRNG                                                       */
    /*-----------------------------------------------------------------------------------------------------*/
    static uint64_t x = 0x500DF00D;
    x ^= x << 13;
    x ^= x >> 7;
    x ^= x << 17;
    return x;
}
